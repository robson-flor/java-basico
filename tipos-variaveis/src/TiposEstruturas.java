/**
 * TiposEstruturas
 */
public class TiposEstruturas {

  public static void main(String[] args) {

    for (int i = 1; i < 10; i++) {
      System.out.println("i = " + i);
    }

    int i = 1;
    while (i < 10) {
      System.out.println("i = " + i);
      i++;
    }

    int ii = 1;
    do {
      System.out.println("ii = " + ii);
      ii++;
    } while (ii < 10);
  }
}
